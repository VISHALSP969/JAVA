
public class ArithmeticOperators {

	public static void main(String[] args) {
		int x=100+50;
		System.out.println(x);
		
		int sum1 = 100 + 50;
	    int sum2 = sum1 + 250;
	    int sum3 = sum2 + sum2;
	    System.out.println(sum1);
	    System.out.println(sum2);
	    System.out.println(sum3);
	    
	    int a = 5;
	    int b = 3;
	    System.out.println(a + b);
	    
	    a = 5;
	    b = 3;
	    System.out.println(a - b);
	    
	    a = 5;
	    b = 3;
	    System.out.println(a * b);
	    
	    a = 10;
	    b = 3;
	    System.out.println(a / b);
	    
	    a = 5;
	    b = 2;
	    System.out.println(a % b);
	    
	    x = 5;
	    ++x;
	    System.out.println(x);
	    
	    --x;
	    System.out.println(x);
	}

}
