
public class Math {

	public static void main(String[] args) {
		System.out.println(java.lang.Math.max(5, 10));
		System.out.println(java.lang.Math.min(5, 10));
		System.out.println(java.lang.Math.sqrt(64));
		System.out.println(java.lang.Math.abs(-4.7));
		
		//Random numbers
		System.out.println(java.lang.Math.random());
		System.out.println(java.lang.Math.random()*101);
	}
}
